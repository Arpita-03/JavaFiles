/*
 Decrementing for loop
 */
package firstjavaprogram;


public class DecrementingForLoop19 {

    
    public static void main(String[] args) {
        
        for(int i=5; i>0; i--)                //It will check the condition as 5 greater than 0, 
         {                                    //then it will decrease to 4 then again check  the condition and
            System.out.println(i);            //so on until condition will become false.
        }                                      
                                              
        
    }
    
}
