/*
Do-While Loop
*/
package firstjavaprogram;


public class DoWhileLoop17 {



    public static void main(String[] args) {
        int a = 0;
      
        do{
            System.out.println(a);         //First it will print the value of a that is 1,
            a++;                          // then after printing a it will become 2 
        }                                 //then it will check the condition and as 2 is less than 5 
        while(a<5);                      //it will then proceed and the process goes till value of a become 5.
            
    
    
    }
    
}

